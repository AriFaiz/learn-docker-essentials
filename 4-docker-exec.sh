docker container exec -i -t app1 /bin/bash //perintah untuk masuk kedalam container app1 dan mengeksekusi program bash script pada container tersebut

ket : 
    -i --> argument interaktif, menjaga input tetap aktif
    -t --> argument untuk alokasi pseudo-TTY(terrminal akses)
    /bin/bash --> contoh kode program yang terdapat didalam container app1  